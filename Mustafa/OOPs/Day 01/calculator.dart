class Calculator {
  num first_number = 0;
  num second_number = 0;

  num add() {
    return first_number + second_number;
  }
  num subtract() {
    return first_number - second_number;
  }
  num multiply() {
    return first_number * second_number;
  }
  num divide() {
    return first_number / second_number;
  }
}
