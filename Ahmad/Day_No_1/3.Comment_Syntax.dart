
/*
Q.No.1:- how to write comment in dart?

Answer:- 

There are two possible way to write comment in dart:- 

Let see:- 

1. 
If you want to write single comment in dart then :- 
 just use two forward slashs like this "//" 

For Example:- 

// This is a comment.

2.
If you want to write multiple lines comment then we use it like this:-

 starting comment with this "/*" 
 And end on this "*/"

For Example:- 

/* A teacher plays a very important life in shaping your life as well as career.
 A good teacher is a blessing for the students in their early years and helps
them understand the world; learn moral values along with education. */

*/



